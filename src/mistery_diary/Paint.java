    private void panel_dibujoMouseDragged(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panel_dibujoMouseDragged
        // TODO add your handling code here:
          if(b1==false){
              b1=true;
              Point p=new Point(evt.getPoint());
             x0=p.x;
             y0=p.y;
          }
        
          Point z=new Point(evt.getPoint()); 
          x1=z.x;
          y1=z.y;
          
    }//GEN-LAST:event_panel_dibujoMouseDragged

    private void panel_dibujoMouseReleased(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_panel_dibujoMouseReleased
        // TODO add your handling code here:
        b1=false;
        int anchura = Math.abs(x1-x0);
        int altura = Math.abs(y1-y0);
        dibujo.dibujarCuadrado(panel_dibujo.getGraphics(), Math.min(x0,x1), Math.min(y0,y1), altura, anchura);
    }//GEN-LAST:event_panel_dibujoMouseReleased

